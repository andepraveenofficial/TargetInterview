
# Reverse a List

given_list = [1, 2, 3, 4, 5]  # list(map(int, input().split()))
print(given_list)

print("-----------------")

reversed_list = given_list[::-1]
print(reversed_list)

print("-----------------")

given_list.reverse()
print(given_list)

print("-----------")

output = list(reversed(given_list))
print(output)

print("================")